import pandas as pd
import streamlit as st
import joblib

# load model
model = joblib.load("model.pkl")

st.title(" Taxi Fare Prediction")

st.write("Enter trip details")

# Inputs

pickup_datetime = st.datetime_input("Pickup Time")
dropoff_datetime = st.datetime_input("Dropoff Time")

passenger_count = st.number_input(
    "Passenger Count",
    min_value=1,
    max_value=6,
    value=1
)

trip_distance = st.number_input(
    "Trip Distance (miles)",
    min_value=0.0,
    value=1.0
)

VendorID = st.selectbox("Vendor ID", [1,2])

RatecodeID = st.selectbox("Rate Code", [1,2,3,4,5,6])

store_and_fwd_flag = st.selectbox(
    "Store and Forward Flag",
    ["N","Y"]
)

payment_type = st.selectbox(
    "Payment Type",
    [1,2,3,4]
)

extra = st.number_input("Extra Charges", value=0.0)

tip_amount = st.number_input("Tip Amount", value=0.0)

tolls_amount = st.number_input("Tolls Amount", value=0.0)


improvement_surcharge = st.number_input(
    "Improvement Surcharge",
    value=0.3
)

PULocationID = st.number_input("Pickup Location ID", value=1)

DOLocationID = st.number_input("Dropoff Location ID", value=1)


# Create dataframe

input_data = pd.DataFrame({
    "VendorID":[VendorID],
    "tpep_pickup_datetime":[pickup_datetime],
    "tpep_dropoff_datetime":[dropoff_datetime],
    "passenger_count":[passenger_count],
    "trip_distance":[trip_distance],
    "RatecodeID":[RatecodeID],
    "store_and_fwd_flag":[store_and_fwd_flag],
    "PULocationID":[PULocationID],
    "DOLocationID":[DOLocationID],
    "payment_type":[payment_type],
    "extra":[extra],
    "tip_amount":[tip_amount],
    "tolls_amount":[tolls_amount],
    "improvement_surcharge":[improvement_surcharge]
})


# Prediction

if st.button("Predict Fare"):

    prediction = model.predict(input_data)

    st.success(f"Predicted Taxi Fare: ${prediction[0]:.2f}")

