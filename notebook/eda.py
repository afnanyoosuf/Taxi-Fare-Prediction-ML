# Import Libraries
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

sns.set(style="whitegrid")

# Load Dataset


df = pd.read_csv("train.csv")

print("Dataset Shape:", df.shape)
print("\nFirst 5 rows:")
print(df.head())

# 3 Dataset Information

print("\nDataset Info")
df.info()

print("\nStatistical Summary")
print(df.describe())

# 4 Check Missing Values

missing = df.isnull().sum().sort_values(ascending=False)
missing_percent = (df.isnull().sum() / len(df)) * 100

print("\nMissing Values Count")
print(missing)

print("\nMissing Values Percentage")
print(missing_percent.sort_values(ascending=False))

# 5 Remove Negative Fare Values


print("\nNegative total_amount rows:",
      df[df['total_amount']<0].shape)
df = df[df['total_amount']>=0]
print("Shape after removing negative fares:",df.shape)

# 6 Identify Feature Types

num_cols = df.select_dtypes(include=['int64','float64']).columns
cat_cols = df.select_dtypes(include=['object']).columns

print("\nNumerical Columns:")
print(num_cols)

print("\nCategorical Columns:")
print(cat_cols)

# 7 Convert Datetime Columns

df['tpep_pickup_datetime'] = pd.to_datetime(df['tpep_pickup_datetime'])
df['tpep_dropoff_datetime'] = pd.to_datetime(df['tpep_dropoff_datetime'])

# 8 Feature Engineering for Analysis

df['trip_duration'] = (df['tpep_dropoff_datetime'] - df['tpep_pickup_datetime']).dt.total_seconds()/60
df['pickup_hour'] = (df['tpep_pickup_datetime']).dt.hour
df['pickup_day'] = (df['tpep_pickup_datetime']).dt.dayofweek

# 9 Distribution Plots

plt.figure(figsize=(6,4))
sns.histplot(df["total_amount"])
plt.xlim(0, 150)
plt.title("Total Amount Distribution")

plt.figure(figsize=(6,4))
sns.histplot(df["passenger_count"], kde=True)
plt.title("Passenger Count Distribution")

df = df[(df["trip_distance"] > 0) & (df["trip_distance"] < 100)]

plt.figure(figsize=(6,4))
sns.histplot(df['trip_distance'], bins=50, kde=True)
plt.xlim(0,50)
plt.title("Trip Distance Distribution")
plt.show()

# 10 Boxplots (Outlier Detection)

plt.figure()
sns.boxplot(x=df['trip_distance'])
plt.title('Trip Distance Boxplot')

plt.figure()
sns.boxplot(x=df['total_amount'])
plt.title('Total Amount Boxplot')

# 11 Correlation Matrix

plt.figure(figsize=(18,14))
sns.heatmap(df.corr(numeric_only=True),annot=True,cmap='coolwarm')
plt.title('Correlation Matrix')

# 12 Relationship with Target

plt.figure()
sns.scatterplot(x=df['trip_distance'],y=df['total_amount'])
plt.title('Trip Distance vs Total Amount')

plt.figure()
sns.scatterplot(x=df['trip_duration'],y=df['total_amount'])
plt.title('Trip Duration vs Total Amount')

# 13 EDA Insights

print("""
EDA INSIGHTS

1. Trip distance has a strong positive relationship with total_amount.
2. Longer trip duration generally results in higher taxi fares.
3. Some outliers exist in trip_distance and total_amount.
4. Most taxi trips are short distance rides.
5. Time features like pickup_hour may influence demand and fare patterns.
""")

